package nested_Loop;

public class nl {

	public static void main(String[] args) {
		int n=6,n1=6;
		for(int m=1; m<n; m++) {
			for(int o=1; o<=m; o++) {
				System.out.print("p");
			}
			System.out.println();
		}
		for(int m=1; m<n1; m++) {
			for(int o=1; o<=m; o++) {
				System.out.print("p");
			}
			System.out.println();
		}
	}

}
