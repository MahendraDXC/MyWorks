package Asnop;

public class Assignment_operators 
{
public static void main(String[] ags){
	System.out.println("Assignment operatons are below \n "
			+ "------------------------------");
	int a=5;
	System.out.println("a       :\t"+a);
	a +=2;
	System.out.println("a+=2    :\t"+a);
	int b=5;
	b -=2;
	System.out.println("b-=2    :\t"+b);
	int c=5;
	c *=2;
	System.out.println("c*=2    :\t"+c);
	int d=5;
	d /=2;
	System.out.println("d/=2    :\t"+d);
	int e=5;
	e %=2;
	System.out.println("e%=2    :\t"+e);
}
}