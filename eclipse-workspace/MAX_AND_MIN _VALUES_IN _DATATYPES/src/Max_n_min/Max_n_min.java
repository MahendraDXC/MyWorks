package Max_n_min;

public class Max_n_min {

	public static void main(String[] args) {
		System.out.println("MAXIMUM AND MINIMUM VALUES OF DATA TYPES \n"+
	"---------------------------------------------------------------------------------");
		System.out.println("(1)     FOR BYTE     -->   :"+Byte.MIN_VALUE +"                  TO  "+Byte.MAX_VALUE);
		System.out.println("(2)     FOR INTEGERS -->   :"+Integer.MIN_VALUE +"           TO  "+Byte.MAX_VALUE);
		System.out.println("(3)     FOR SHORT    -->   :"+Short.MIN_VALUE +"                TO  "+Short.MAX_VALUE);
		System.out.println("(4)     FOR LONG     -->   :"+Long.MIN_VALUE +"  TO  "+Long.MAX_VALUE );
		System.out.println("(5)     FOR FLOAT    -->   :"+Float.MIN_VALUE +"               TO  "+Float.MAX_VALUE );
		System.out.println("(6)     FOR DOUBLE   -->   :"+Double.MIN_VALUE +"              TO  "+Double.MAX_VALUE );
		System.out.println("(7)     FOR CHAR     -->   :"+Character.MIN_VALUE +" TO  "+Character.MAX_VALUE );
		//System.out.println("(8)       FOR BOOLEAN-->   :"+Boolean.MIN_VALUE +"    TO  "+Boolean.MAX_VALUE );
		}

}
