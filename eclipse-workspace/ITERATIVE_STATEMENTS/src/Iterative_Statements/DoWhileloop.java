package iterative_Statements;

public class DoWhileloop {

	public static void main(String[] args) {
		System.out.println("\n\tDO WHILE OPERATION\n"+"-----------------------------------\n");
		int i=0;
		do
		{
			
			System.out.print(i+" ");
			i++;
		}while (i<= 'm'+'a'+'h'+'e'+'n'+'d'+'r'+'a');
		System.out.println("\ncan u reach me? ");

	}

}
